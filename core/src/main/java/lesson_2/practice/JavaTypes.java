package lesson_2.practice;

public class JavaTypes {
    public static void main(String[] args) {
        //1 type name = number
        byte myByte = 2;

        //2
        byte myByte2;
        myByte2 = 44;

        byte myByte3 = 127;
        System.out.println(myByte3);

        short myShort = 33;
        System.out.println(myShort);

        int myInt = 2123333111;
        long myLong = 222222222222222L;
        System.out.println(myLong);

        float myFloat = 2.15f;
        System.out.println(myFloat);
        float myFloat2 = 10;
        System.out.println(myFloat2);

        double myDouble = 55.89;
        double myDouble2 = 55;
        System.out.println(myDouble);
        System.out.println(myDouble2);

        boolean myBoo = true;
        System.out.println(myBoo);

        char myChar = 'K';
        System.out.println(myChar);

        char myCharUtf = '\n';
        char myCharNum = 33;

        System.out.println(myCharUtf);
        System.out.println(myCharNum);
    }
}
